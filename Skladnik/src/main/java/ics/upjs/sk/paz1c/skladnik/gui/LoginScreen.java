    private boolean passwordIsCorrect(char[] input){
     boolean isCorrect = false;
     char[] correctPassword = this.correctPassword;
     if(input.length != correctPassword.length){
     return false;
     } else {
     isCorrect = Arrays.equals(input, correctPassword);
     }
     
        
     
    return isCorrect;
    }
